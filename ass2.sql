SELECT NAME, DEPT_NAME
FROM STUDENT2
INNER JOIN DEPARTMENT
ON STUDENT2.DEPT_ID = DEPARTMENT.DEPT_ID;

SELECT COURSE_NAME
FROM COURSE
LEFT JOIN STUDENT2
ON STUDENT2.COURSE_ID = COURSE.COURSE_ID
WHERE COURSE.COURSE_ID NOT IN (SELECT COURSE_ID FROM STUDENT2);

SELECT NAME, STUDENT2.STATUS_ID
FROM STUDENT2
LEFT JOIN STUDENT_STATUS
ON STUDENT_STATUS.STATUS_ID = STUDENT2.STATUS_ID
WHERE STUDENT2.STATUS_ID NOT IN (SELECT STATUS_ID FROM STUDENT_STATUS);

